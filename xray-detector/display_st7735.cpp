void display_st7735_init(uint8_t dc) {
  display_spi_init(dc);

  display_st7735_run_command_list(DISPLAY_ST7335_COMMAND_LIST1);
  display_st7735_run_command_list(DISPLAY_ST7335_COMMAND_LIST2);
  display_st7735_run_command_list(DISPLAY_ST7335_COMMAND_LIST3);

  uint8_t madctl = 0xC0;
  display_spi_send_command(ST77XX_MADCTL, &madctl, 1);

  madctl = ST77XX_MADCTL_MX | ST77XX_MADCTL_MY | ST77XX_MADCTL_RGB;
  display_spi_send_command(ST77XX_MADCTL, &madctl, 1);

  madctl = ST77XX_MADCTL_MX | ST77XX_MADCTL_MV | ST77XX_MADCTL_RGB;
  display_spi_send_command(ST77XX_MADCTL, &madctl, 1);
}

void display_st7735_run_command_list(const uint8_t *addr) {

  uint8_t numCommands, cmd, numArgs;
  uint16_t ms;

  numCommands = pgm_read_byte(addr++); // Number of commands to follow
  while (numCommands--) {              // For each command...
    cmd = pgm_read_byte(addr++);       // Read command
    numArgs = pgm_read_byte(addr++);   // Number of args to follow
    ms = numArgs & ST_CMD_DELAY;       // If hibit set, delay follows args
    numArgs &= ~ST_CMD_DELAY;          // Mask out delay bit
    display_spi_send_command_pgm(cmd, addr, numArgs);
    addr += numArgs;

    if (ms) {
      ms = pgm_read_byte(addr++); // Read post-command delay time (ms)
      if (ms == 255)
        ms = 500; // If 255, delay for 500 ms
      clock_delay(ms);
    }
  }
}
